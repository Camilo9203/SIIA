		<div id="question1" class="panel-collapse collapse" style="height: 0px;">
			<div class="panel-body">
				<p>Es el proceso mediante el cual la Unidad Administrativa Especial de Organizaciones Solidarias avala la calidad y pertinencia de los programas en educación solidaria. Con la acreditación, la Unidad Administrativa Especial de Organizaciones Solidarias autoriza a las instituciones jurídicas sin ánimo de lucro y a las entidades públicas en cuyo objeto se encuentre determinada la prestación de servicios educativos la posibilidad de impartir programas de educación solidaria. La Acreditación es necesaria para impartir los Cursos Básicos de Economía Solidaria. La Coordinación de Educación e Investigación de la Unidad Administrativa Especial de Organizaciones Solidarias realiza una evaluación, emite el concepto respectivo y expide el acto administrativo correspondiente para la Acreditación.
					Con la Acreditación, se busca que las instituciones acreditadas cumplan con requisitos de calidad en la formación impartida y desarrollen los fines propios de la educación. También se busca fortalecer los procesos de formación que adelantan las instituciones para impartir educación solidaria y cualificar su gestión en aspectos básicos referidos a la enseñanza, desempeño profesional de los docentes vinculados, eficacia de los métodos pedagógicos, prestación del servicio, organización administrativa, infraestructura física e implementación del modelo solidario.</p>
			</div>
