		<div id="question2" class="panel-collapse collapse" style="height: 0px;">
			<div class="panel-body">
				<p>Se denomina Sistema Integrado de Información de Acreditación -SIIA- al aplicativo virtal que permite realizar la captura de la información requerida por la Unidad Administrativa Especial de Organizaciones Solidarias para el desarrollo del trámite de Acreditación; es decir permite el proceso de Acreditación a través de Internet. Es importante resaltar que la Unidad Administrativa Especial de Organizaciones Solidarias implementa este trámite en línea para realizar el proceso de Acreditación y también para obtener datos básicos sobre las características de las entidades que se están acreditando. El SIIA permite el desarrollo del trámite de una manera ágil reduciendo tiempos en la evaluación y la retroalimentación de ésta, además de concretar los contenidos y requisitos que se exigen dentro del proceso.</p>
			</div>
